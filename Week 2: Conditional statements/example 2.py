# Basic arithmetic operations in Python

# Addition
result_add = 5 + 3
print("Addition:", result_add)

# Subtraction
result_sub = 10 - 4
print("Subtraction:", result_sub)

# Multiplication
result_mul = 6 * 7
print("Multiplication:", result_mul)

# Division
result_div = 20 / 5
print("Division:", result_div)

# Exponentiation
result_exp = 2 ** 3
print("Exponentiation:", result_exp)
